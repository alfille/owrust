use std::convert::TryInto;
use std::ops::Deref;

#[derive(Debug)]
pub struct RomId([u8; 8]);

impl Deref for RomId {
    type Target = [u8; 8];

    fn deref(&self) -> &Self::Target {
        &self.0
    }
}

