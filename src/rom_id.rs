pub fn crc8(bytes: &[u8]) -> u8 {
    bytes.iter()
        .fold(0u8, |crc, &byte| CRC8TABLE[(crc ^ byte) as usize])
}
pub fn crc8_seeded(bytes: &[u8], seed: u8) -> u8 {
    bytes.iter()
        .fold(seed, |crc, &byte| CRC8TABLE[(crc ^ byte) as usize])
}

impl RomId {
    /// ### create a ROM id structure
    /// This is an 8byte array holding the unique 1-wire address
    /// * byte array (or vector) accepted
    /// * first 8 bytes copied directly
    /// * if only 7 bytes available, the CRC8 is generated automatically
    /// * else an all-zero ROM is created (useless)
    pub fn new<B>(data: B) -> Self
    where
        B: AsRef<[u8]>,
    {
        let mut rom = [0u8; 8];
        let bytes = data.as_ref();
        if bytes.len() > 7 {
            rom.copy_from_slice(&bytes[0..8]);
        } else if bytes.len() == 7 {
            rom[0] = bytes[0];
            rom[1] = bytes[1];
            rom[2] = bytes[2];
            rom[3] = bytes[3];
            rom[4] = bytes[4];
            rom[5] = bytes[5];
            rom[6] = bytes[6];
            rom[7] = crc8(bytes);
        }
        Self(rom)
    }
    /// Get family code (first byte)
    pub fn family(&self) -> u8 {
        self[0]
    }
    /// id is the middle 6 bytes (excluding family code and crc8 -- it's the unique part
    pub fn id(&self) -> [u8; 6] {
        self[1..7].try_into().unwrap()
    }
    /// Get crc8 check byte
    pub fn crc8(&self) -> u8 {
        self[7]
    }
    /// Check that crc8 byte is correct
    pub fn test_crc8(&self) -> bool {
        crc8(&self.0) == 0u8
    }
    /// Create the crc8
    pub fn make_crc8(&self) -> u8 {
        crc8(&self[0..7])
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    #[test]
    /// Ignores last byte
    fn t_rom9() {
        let data = [0x10, 0x67, 0xc6, 0x69, 0x73, 0x51, 0xff, 0x8d, 0xFF];
        let rom = RomId::new(data);
        assert_eq!(rom.family(), 0x10);
        assert_eq!(rom.crc8(), 0x8d);
        assert!(rom.test_crc8());
        assert_eq!(rom.make_crc8(), 0x8d);
        assert_eq!(rom.id(), [0x67, 0xc6, 0x69, 0x73, 0x51, 0xff]);
    }
    #[test]
    /// exact rom id
    fn t_rom8() {
        let data = [0x10, 0x67, 0xc6, 0x69, 0x73, 0x51, 0xff, 0x8d];
        let rom = RomId::new(data);
        assert_eq!(rom.family(), 0x10);
        assert_eq!(rom.crc8(), 0x8d);
        assert!(rom.test_crc8());
        assert_eq!(rom.make_crc8(), 0x8d);
        assert_eq!(rom.id(), [0x67, 0xc6, 0x69, 0x73, 0x51, 0xff]);
    }
    #[test]
    /// creates crc8
    fn t_rom7() {
        let data = [0x10, 0x67, 0xc6, 0x69, 0x73, 0x51, 0xff];
        let rom = RomId::new(data);
        assert_eq!(rom.family(), 0x10);
        assert_eq!(rom.crc8(), 0x8d);
        assert!(rom.test_crc8());
        assert_eq!(rom.make_crc8(), 0x8d);
        assert_eq!(rom.id(), [0x67, 0xc6, 0x69, 0x73, 0x51, 0xff]);
    }
    #[test]
    /// too short
    fn t_rom6() {
        let data = [0x10, 0x67, 0xc6, 0x69, 0x73, 0x51];
        let rom = RomId::new(data);
        assert_eq!(rom.0, [0u8; 8]);
        assert_eq!(rom.family(), 0x00);
        assert_eq!(rom.crc8(), 0x00);
        assert!(rom.test_crc8());
        assert_eq!(rom.make_crc8(), 0x00);
        assert_eq!(rom.id(), [0u8; 6]);
    }
    #[test]
    /// empty
    fn t_rom0() {
        let data = Vec::<u8>::new();
        let rom = RomId::new(data);
        assert_eq!(rom.0, [0u8; 8]);
        assert_eq!(rom.family(), 0x00);
        assert_eq!(rom.crc8(), 0x00);
        assert!(rom.test_crc8());
        assert_eq!(rom.make_crc8(), 0x00);
        assert_eq!(rom.id(), [0u8; 6]);
    }
}
