//! Rom Id code
//! Each 1-wire device has a unique address
//! * 1 byte "family ocde" -- chip type
//! * 6 byte unique id
//! * 1 byte check byte (crc8)
//!
//! Example for RomId
//! ```
//! use owrust::rom_id::RomId ;
//! let rom = RomId::new( &[0x05,0x4A,0xEC,0x29,0xCD,0xDA,0xAB] );
//! let family = rom.family();
//! assert_eq!(rom.make_crc8(),0x18);
//! ```
//! Example for utility function crc8
//! ```
//! use owrust::rom_id::crc8;
//! let c:u8 = crc8( &[0x10, 0x67, 0xc6, 0x69, 0x73, 0x51, 0xff, 0x8d] ) ;
//! assert_eq!(c,0x00);
//! ```
//!

